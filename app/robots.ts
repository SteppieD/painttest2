import { MetadataRoute } from 'next'

export default function robots(): MetadataRoute.Robots {
  const baseUrl = process.env.NEXT_PUBLIC_SITE_URL || 'https://propaintsolutions.com'
  
  return {
    rules: {
      userAgent: '*',
      allow: '/',
      disallow: [
        '/api/',
        '/admin/',
        '/_next/',
        '/dashboard/',
        '/quotes/*/edit'
      ]
    },
    sitemap: `${baseUrl}/sitemap.xml`
  }
}